public class StringExercise {

    public static void main(String[] args) {

//        1. String basics

        String pinkFloyd = "We don't need no education\n" +
                "We don't need no thought control";

        System.out.println(pinkFloyd);

        String checkThis = "Check \"this\" out!, \"s inside of \"s!";

        System.out.println(checkThis);

        String cDrive = "In windows, the main drive is usually C:\\";

        System.out.println(cDrive);

        String hackNSlash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";

        System.out.println(hackNSlash);

    }

}
